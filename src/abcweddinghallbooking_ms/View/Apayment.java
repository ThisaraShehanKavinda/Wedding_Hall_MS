    public void Load() {
        BookingDAOImp dao = new BookingDAOImp();
        List<Booking> list = dao.list();
        DefaultTableModel dft = (DefaultTableModel) tblBooking.getModel();
        dft.setRowCount(0);
        for (Booking book : list) {
            int bookid = book.getBooking_id();
            int customerid = book.getCustomer_id();
            int hallid = book.getHall_id();
            int priceperday = book.getPrice_per_day();
            Date checkin = book.getCheck_in();
            Date checkOut = book.getCheck_out();
            int totalAmount = book.getTotal_amount();
            String paymentType = book.getPayment_type();
            String payment = book.getPayment();
            

            dft.addRow(new Object[]{bookid, customerid, hallid, priceperday, checkin, checkOut, totalAmount, paymentType, payment});
        }
    }
    
    
    
    

