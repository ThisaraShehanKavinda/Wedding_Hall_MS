    private void btnAdd1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdd1ActionPerformed

        // Get the input from the txtID textbox
String idText = txtID.getText();

// Define the regex pattern for integers
String integerPattern = "^[0-9]+$";

// Validate the input against the pattern
if (idText.matches(integerPattern)) {
    if (txtID.getText().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Empty ID field", "Error", JOptionPane.ERROR_MESSAGE);
} else {
    try {
        int id = Integer.parseInt(txtID.getText());
        // Continue with the code using the id
        BookingDAOImp dao = new BookingDAOImp();
        Booking book = dao.get(id);
        
        txtCustomerID.setText(String.valueOf(book.getCustomer_id()));
        txtHallID.setText(String.valueOf(book.getHall_id()));
        txtPayment.setText(book.getPayment());
        
        String paymentStatus = txtPayment.getText();
        
        if (paymentStatus.equalsIgnoreCase("pending")) {
            btnPay.setVisible(true);
        } else {
            btnPay.setVisible(false);
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid ID. Please enter a valid integer.", "Error", JOptionPane.ERROR_MESSAGE);
    }
}
}
    
    else {
    JOptionPane.showMessageDialog(this, "Invalid ID type!");
}
        
        
        
        
        
       


       

    }//GEN-LAST:event_btnAdd1ActionPerformed


