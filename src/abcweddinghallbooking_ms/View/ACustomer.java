    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed


if(txtAge.getText().isEmpty()|| txtContactNo.getText().isEmpty() || txtCustomerID6.getText().isEmpty() || txtEmail.getText().isEmpty() || txtFullName.getText().isEmpty() || txtID.getText().isEmpty() || txtNicNo.getText().isEmpty() || txtaddress.getText().isEmpty() || cmbGender.getSelectedItem().equals(null) || DateDateOfBirth.getDatoFecha().equals(null)){
    JOptionPane.showMessageDialog(this, "Fields are empty");
}        else{
    Customer cs = new Customer();
       // int customer_id = Integer.parseInt(txtID.getText());
        String name = txtFullName.getText();
        Date date_of_birth = DateDateOfBirth.getDatoFecha();
        int age = Integer.parseInt(txtAge.getText());
        String gender = cmbGender.getSelectedItem().toString();
        String nic_no = txtNicNo.getText();
        String contact_no = txtContactNo.getText();
        String email = txtEmail.getText();
        String address = txtaddress.getText();

        cs.setName(name);
        cs.setDate_of_birth(date_of_birth);
        cs.setAge(age);
        cs.setGender(gender);
        cs.setNic_no(nic_no);
        cs.setContact_no(contact_no);
        cs.setEmail(email);
        cs.setAddress(address);
        //cs.setCustomer_id(customer_id);

        CustomerDAOImp dao = new CustomerDAOImp();
        dao.save(cs);
        Load();
        txtFullName.setText("");
        txtAge.setText("");
        txtContactNo.setText("");
        txtEmail.setText("");
        txtNicNo.setText("");
        txtaddress.setText("");
        cmbGender.setSelectedItem("");
}

        

    }//GEN-LAST:event_btnAddActionPerformed

