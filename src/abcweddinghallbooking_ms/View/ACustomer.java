    private void tblCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCustomerMouseClicked
        
        int rowNo = tblCustomer.getSelectedRow();
        TableModel model = tblCustomer.getModel();

        txtID.setText(model.getValueAt(rowNo, 0).toString());
        txtFullName.setText(model.getValueAt(rowNo, 1).toString());
        Object dateValue = model.getValueAt(rowNo, 2);
        if (dateValue instanceof Date) {
            Date dateOfBirth = (Date) dateValue;
            DateDateOfBirth.setDatoFecha(dateOfBirth); // Set the selected date
        }
        txtAge.setText(model.getValueAt(rowNo, 3).toString());
        cmbGender.setSelectedItem(model.getValueAt(rowNo, 4).toString());
        txtNicNo.setText(model.getValueAt(rowNo, 5).toString());
        txtContactNo.setText(model.getValueAt(rowNo, 6).toString());
        txtEmail.setText(model.getValueAt(rowNo, 7).toString());
        txtaddress.setText(model.getValueAt(rowNo, 8).toString());
                                            
    }//GEN-LAST:event_tblCustomerMouseClicked

