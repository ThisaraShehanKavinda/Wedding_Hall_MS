     private String generateCaptchaCode() {
        String codeChars = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789";
        StringBuilder captchaCodeBuilder = new StringBuilder();
        for (int i = 0; i < 6; i++) {
            int randomIndex = (int) (Math.random() * codeChars.length());
            captchaCodeBuilder.append(codeChars.charAt(randomIndex));
        }
        return captchaCodeBuilder.toString();
        
    }
    
     
     
    //check for duplicate users
      public boolean checkDuplicateUser(){
        String name =  txtName.getText();
        boolean isExist = false;
        
        
        try{
            
            
            Connection con = DBConnection.getConnection();
            
            PreparedStatement pst = con.prepareStatement("Select * from admin where name =?");
            pst.setString(1, name);
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                isExist = true;
            }
            else{
                isExist=false;
            }
            
        }
        catch(Exception ex){
            ex.getStackTrace();
        }
       return isExist;
    }   
    
      
