    private void JButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonActionPerformed
        String enteredCode = captchaTextBox.getText().trim();
        String captchaCode = captchaLabel.getText().trim();
        if (enteredCode.equalsIgnoreCase(captchaCode)) {
            JOptionPane.showMessageDialog(this, "Captcha code matched!", "Success", JOptionPane.INFORMATION_MESSAGE);
            btnRegister.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "Captcha code not matched!", "Error", JOptionPane.ERROR_MESSAGE);
            captchaLabel.setText(generateCaptchaCode());
            btnRegister.setEnabled(false);
        }
        captchaTextBox.setText("");

    }//GEN-LAST:event_JButtonActionPerformed

